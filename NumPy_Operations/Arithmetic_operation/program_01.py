# NumPy multiply

import numpy as np

a=np.arange(12).reshape(3,4)
print(a)
print('Multiply result is :','\n',a*2)

# Numpy add
print('Addition result is :','\n',a+2)

# Numpy sub
print('Subtraction result is :','\n',a-2)

#Numpy division
print('division result is :','\n',a/2)

# Numpy power
print('power result is :','\n',a**2)


